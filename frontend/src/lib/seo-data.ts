function isTemplatePlaceholderOfficialSource(url: string | undefined): boolean {
  if (!url) return false;

  try {
    const parsed = new URL(url);
    const pathname = parsed.pathname.toLowerCase().replace(/\/+$/, '');
    return parsed.hostname === 'www.state.gov' && /^\/[a-z]{2}$/.test(pathname);
  } catch {
    return false;
  }
}

function isTemplateSourceUnderReviewName(name: string | undefined): boolean {
  return (
    typeof name === 'string' &&
    (
      name.toLowerCase().includes('template source under review') ||
      name.toLowerCase().includes('source link under review')
    )
  );
}

