function resolveCountiesServed(repair) {
  if (repair.counties_served) return repair.counties_served;
  if (!repair.statewide_counties_state_id) return '';

  return db.prepare(`
    SELECT GROUP_CONCAT(id, ',') AS counties_served
    FROM (
      SELECT id
      FROM counties
      WHERE state_id = ?
      ORDER BY id
    )
  `).get(repair.statewide_counties_state_id).counties_served || '';
}

